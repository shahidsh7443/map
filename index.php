                                   <div class="item">
                                      <p>Ooty (Ootacamund)  the Queen of the Hill Stations, also known as Blue Mountains is one of the most beautiful hill stations in India. This hill station has  splendid green downs and lofty hills of extraordinary scenic beauty and is endowed with a salubrious climate similar to the English summer.</p>
                                      <p>Located in the Western Ghats at a height of 7,500ft, Oooty/ Udhagamandalam is the headquarters of the Nilgiri district. Ooty was the former summer retreat of the British. John Sullivan, the collector of Coimbatore who founded the first European settlement here, stumbled across Ooty's idyllic scenery in 19th century during one of his nomadic searches for rare flowers. </p>
                                   </div>
                                   <div class="item">
                                      <p>The name Ooty or Ootacamund derives its name from the local Toda word Udhamandalam meaning village of huts.  The Tamil 'Whotai'  a species of dwarf bamboo that grows in the forests, 'kai' meaning a fruit or green stuff in the middle and the Toda 'mund' meaning a village clapped at the end.</p>
                                      <p>Ooty, however, got its name during the British rule, by calling 'WOTYKAMUND' in the Madras gazette.</p>
